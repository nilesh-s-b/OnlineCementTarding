A cement is a binder, a substance used for construction that sets, hardens and adheres to other materials, binding them together.
Cement is seldom used on its own, but rather to bind sand and gravel (aggregate) together.
Cement is used with fine aggregate to produce mortar for masonry, or with sand and gravel aggregates to produce concrete.

